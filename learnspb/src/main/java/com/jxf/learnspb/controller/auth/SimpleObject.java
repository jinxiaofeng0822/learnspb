package com.jxf.learnspb.controller.auth;

import java.util.Date;

public class SimpleObject {

    private Integer aaa;
    private Double bbb;
    private String ccc;
    private Date ddd;

    public Integer getAaa() {
        return aaa;
    }

    public void setAaa(Integer aaa) {
        this.aaa = aaa;
    }

    public Double getBbb() {
        return bbb;
    }

    public void setBbb(Double bbb) {
        this.bbb = bbb;
    }

    public String getCcc() {
        return ccc;
    }

    public void setCcc(String ccc) {
        this.ccc = ccc;
    }

    public Date getDdd() {
        return ddd;
    }

    public void setDdd(Date ddd) {
        this.ddd = ddd;
    }
}
